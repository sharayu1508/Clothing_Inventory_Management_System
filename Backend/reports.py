def supplier_report():

    conn = get_connection()
    cursor = conn.cursor()

    cursor.execute("""
        SELECT Supplier_ID,
               Supplier_Name,
               Contact_Person,
               Phone,
               Email,
               Address
        FROM Suppliers
        ORDER BY Supplier_ID
    """)

    suppliers = cursor.fetchall()

    if suppliers:

        print("\n========== SUPPLIER REPORT ==========\n")

        print(
            tabulate(
                suppliers,
                headers=SUPPLIER_HEADERS,
                tablefmt="grid"
            )
        )

        print("\nTotal Suppliers :", len(suppliers))

    else:

        print("No Supplier Records Found.")

    cursor.close()
    conn.close()
    # -------------------- SUPPLIER PDF --------------------

