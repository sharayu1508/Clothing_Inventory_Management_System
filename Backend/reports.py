# -------------------- COMMON PDF FUNCTION --------------------

def create_pdf(filename, report_title, headers, data):

    pdf = SimpleDocTemplate(
        filename,
        pagesize=letter
    )

    styles = getSampleStyleSheet()

    elements = []

    # Project Title
    title = Paragraph(
        "<b><font size=18>CLOTHING INVENTORY MANAGEMENT SYSTEM</font></b>",
        styles["Title"]
    )

    elements.append(title)
    elements.append(Spacer(1, 12))

    # Report Title
    report = Paragraph(
        f"<b><font size=14>{report_title}</font></b>",
        styles["Heading2"]
    )

    elements.append(report)
    elements.append(Spacer(1, 10))

    # Date & Time
    current_date = datetime.now().strftime("%d-%m-%Y %I:%M %p")

    date = Paragraph(
        f"<b>Generated On :</b> {current_date}",
        styles["Normal"]
    )

    elements.append(date)
    elements.append(Spacer(1, 15))

    # Table Data
    table_data = [headers]

    for row in data:
        table_data.append(list(row))

    table = Table(table_data)

    table.setStyle(
        TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.darkblue),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),

            ('BACKGROUND', (0, 1), (-1, -1), colors.beige),

            ('GRID', (0, 0), (-1, -1), 1, colors.black),

            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),

            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),

            ('BOTTOMPADDING', (0, 0), (-1, 0), 10),

            ('TOPPADDING', (0, 1), (-1, -1), 8),

            ('FONTSIZE', (0, 0), (-1, -1), 10),
        ])
    )

    elements.append(table)
    elements.append(Spacer(1, 20))

    # Total Records
    total = Paragraph(
        f"<b>Total Records :</b> {len(data)}",
        styles["Heading3"]
    )

    elements.append(total)
    elements.append(Spacer(1, 15))

    # Footer
    footer = Paragraph(
        "<b>Generated by Clothing Inventory Management System</b>",
        styles["Normal"]
    )

    elements.append(footer)

    pdf.build(elements)

    print(f"\n{filename} Generated Successfully.")
